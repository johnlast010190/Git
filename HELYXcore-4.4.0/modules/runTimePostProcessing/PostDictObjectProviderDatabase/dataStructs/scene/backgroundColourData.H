#pragma once

#include "postDict/postDictKeys.H"
#include "db/dictionary/dictionary.H"
#include "meshes/primitiveShapes/point/point.H"


// * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * //

namespace Foam::functionObjects::runTimeVis
{

struct BackgroundColourData
{
    point background1;
    point background2;

    explicit BackgroundColourData(const dictionary& dict)
    {
        background1 = dict.lookupOrDefault(backgroundKeys::BACKGROUND_1_KEY, point(0.9372549019607843,0.9372549019607843,0.9372549019607843));
        background2 = dict.lookupOrDefault(backgroundKeys::BACKGROUND_2_KEY, point(0.8, 0.8, 0.8));
    }
};

// * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * //

} // End namespace

